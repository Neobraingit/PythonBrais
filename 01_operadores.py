### OPERADORES ARITMÉTICOS ###

print (3 + 4) # Operador de suma
print (3 - 4) # Operador de resta
print (3 / 4) # Operador de división
print (3 * 4) # Operador de multiplicación
print (3 % 4 ) # Operador de módulo
print (3 // 4) # Operador de floordivision (Termina aproximada a un número entero)
print (3 ** 4) # Operador de exponente

print ('hola' + 'python') # Concatena las dos cadenas de texto (sin embargo, no podremos restar ni hacer otras operaciones con strings)
print ('hola' + str(5)) # Convertimos un número a un string para poder concatenarlo

### OPERADORES DE COMPARACIÓN ###

print (3 > 4) # Mayor que
print (3 < 4) # Menos que
print (3 >= 4) # Mayor o igual    
print (3 <= 4) # Menor o igual
print (3 == 4) # Igual que
print (3 != 4) # Diferente que

'''Nos sirven también para comparar los strings dependiendo el orden alfabético de las letras'''
print ('hola' == 'Python')
print ('hola' == 'hola')

'''Aunque también podemos contar la longitud de caracteres con la función len'''
print (len('Hola') > len('zola'))

### OPERADORES LÓGICOS ###

print (3 > 4 and 4 == 5) # Y
print (3 or 4) # O
print (not 3 < 4) # Negación

